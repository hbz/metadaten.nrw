	Linked Open Usable Data
</div>

<p>
	Offenheit bedeutet für uns insbesondere die Bereitstellung webkonformer Metadaten. JSON ist
	leichtgewichtig, als Datenformat im Web unangefochten und für Webentwickler:innen leicht nutzbar.
	Mit JSON-LD werden JSON-Daten gleichzeitig zu Linked Data und durch ihre leichte Nutzbarkeit zu
	Linked Open Usabel Data (LOUD).
</p>

