<p>
	Offene Standards sind Grundlage für die maximale Nutzbarkeit von Metadaten. Die Bibliothekswelt
	hat mit MARC21 früh an gemeinsamen domänenspezifischen Standards gearbeitet. Mit Bibframe wurde
	der Weg zu webkonformen Standards eingeschlagen. Wir setzen seit langem auf domänen-übergreifende
	Webstandards, um wartungsarme und zukunftsfähige Anwendungen zu ermöglichen.
</p>

<div
	class="my-[20px] text-fuchsia-700 text-2xl font-normal font-['Open Sans'] uppercase tracking-wide"
>
	Standards Made by Community
</div>
<p>
	Offene Standards bauen auf einer community-gestützten Entwicklung auf. Eine gemeinschaftlich
	umgesetzte Standardisierung ist Voraussetzung, um eine breite Nutzung und Akzeptanz zu erreichen,
	um Interoperabilität zu fördern und Silos zu vermeiden.
</p>
<p>
	Wir engagieren uns im Kontext des Kompetenzzentrums Interoperable Metadaten ([DINI AG
	KIM](https://wiki.dnb.de/display/DINIAGKIM)), des World Wide Web Consortiums
	([W3C](https://www.w3.org/)) und der Dublin Core Metadata Initiative
	([DCMI](https://www.dublincore.org/)) für die gemeinsame Entwicklung und Pflege von
	standardisierten Metadaten und Protokollen.
</p>

<div
	class="my-[20px] text-fuchsia-700 text-2xl font-normal font-['Open Sans'] uppercase tracking-wide"
